import { Inter } from "next/font/google";
import { Metadata } from "next";
import "./globals.css";
import { MenuProvider } from "./contexts/MenuContext";
import Sidebar from "./components/Sidebar";
import { ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";
import { cookies } from "next/headers";
import { getUserFromToken } from "@/utils/GetUserFromToken";
import QueryProvider from "./contexts/QueryProvider";
import { BookingProvider } from "./contexts/BookingContext";

const inter = Inter({
  subsets: ["latin"],
  variable: "--font-inter",
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const cookieStore = await cookies();
  const token = (await cookieStore).get("token")?.value; // Pobieramy wartość tokenu

  let user = null;
  if (token) {
    try {
      user = await getUserFromToken(token); // Pobieramy użytkownika z tokenu
    } catch (error) {
      console.error("Error fetching user from token:", error);
    }
  }

  return (
    <html lang="en" suppressHydrationWarning className={inter.variable}>
      <body className="font-sans">
        <QueryProvider>
          <BookingProvider>
            <div className="flex w-auto">
              <MenuProvider>
                <Sidebar
                  isLoggedIn={!!user} // Przekazujemy informację, czy użytkownik jest zalogowany
                  userData={
                    user
                      ? {
                          username: user.username,
                          avatar: user.avatar || "/default-avatar.png",
                        }
                      : null
                  }
                />

                <main className="flex-grow">{children}</main>
              </MenuProvider>
              <ToastContainer />
            </div>
          </BookingProvider>
        </QueryProvider>
      </body>
    </html>
  );
}
